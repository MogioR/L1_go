import "fmt"

/*
	–†–∞–∑—Ä–∞–±–æ—Ç–∞—Ç—å –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –ø–µ—Ä–µ–≤–æ—Ä–∞—á–∏–≤–∞–µ—Ç –ø–æ
	–¥–∞–≤–∞–µ–º—É—é –Ω–∞ —Ö–æ–¥ —Å—Ç—Ä–æ–∫—É (–Ω–∞–ø—Ä–∏–º–µ—Ä: ¬´–≥–ª–∞–≤—Ä—ã–±–∞ ‚Äî –∞–±—ã—Ä–≤–∞–ª–≥¬ª).
	–°–∏–º–≤–æ–ª—ã –º–æ–≥—É—Ç –±—ã—Ç—å unicode.
*/

// –ü–ª–æ—Ö–æ —Ä–∞–±–æ—Ç–∞–µ—Ç —Å —è–∑—ã–∫–∞–º–∏ –≥–¥–µ –æ–¥–∏–Ω —Å–∏–º–≤–æ–ª –∑–∞–ø–∏—Å—ã–≤–∞–µ—Ç—Å—è –Ω–µ—Å–∫–æ–ª—å–∫–∏–º —Ä—É–Ω–∞–º–∏, –Ω–∞–ø—Ä–∏–º–µ—Ä —Å —Ä—É–Ω–æ–π '
func stringReverse(s string) string {
	runes := []rune(s)

	//–ú–µ–Ω—è–µ–º —Ä—É–Ω—ã –º–µ—Å—Ç–∞–º–∏
	for i, j := 0, len(runes)-1; i < j; i, j = i+1, j-1 {
		runes[i], runes[j] = runes[j], runes[i]
	}
	return string(runes)
}

func main() {
	fmt.Printf("%s", stringReverse("Abbat üòÄ"))
}
